    private void exportToCSV(String filePath) {
        try (BufferedWriter writer = new BufferedWriter(new FileWriter(filePath))) {
            // Write header
            for (int i = 0; i < tblSchedule.getColumnCount(); i++) {
                writer.write(tblSchedule.getColumnName(i));
                if (i < tblSchedule.getColumnCount() - 1) {
                    writer.write(",");
                }
            }
            writer.newLine();

            // Write data rows
            for (int i = 0; i < tblSchedule.getRowCount(); i++) {
                for (int j = 0; j < tblSchedule.getColumnCount(); j++) {
                    writer.write(tblSchedule.getValueAt(i, j).toString());
                    if (j < tblSchedule.getColumnCount() - 1) {
                        writer.write(",");
                    }
                }
                writer.newLine();
            }

            JOptionPane.showMessageDialog(this, "Data berhasil diekspor ke CSV.", "Sukses", JOptionPane.INFORMATION_MESSAGE);
        } catch (IOException e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Gagal mengekspor data: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }

