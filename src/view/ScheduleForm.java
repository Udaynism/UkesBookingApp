    private String convertTimestamp(Timestamp timestamp) {
        java.util.Date date = new java.util.Date(timestamp.getTime());
        java.text.SimpleDateFormat sdf = new java.text.SimpleDateFormat("dd-MM-yyyy HH:mm");
        return sdf.format(date);
    }

    private void loadScheduleData() {
        DefaultTableModel model = (DefaultTableModel) tblSchedule.getModel();
        model.setRowCount(0); // Clear existing rows

        try (Connection conn = SQLiteConnection.getConnection();
             Statement stmt = conn.createStatement();
             ResultSet rs = stmt.executeQuery("SELECT * FROM bookings WHERE status = 'Disetujui'")) {

            while (rs.next()) {
                String nama = rs.getString("name");
                String ukm = rs.getString("ukm");
                String namaKegiatan = rs.getString("activity_name");
                Timestamp start_time = rs.getTimestamp("start_time");
                Timestamp end_time = rs.getTimestamp("end_time");

                String waktuKegiatan = convertTimestamp(start_time) + " s.d. " + convertTimestamp(end_time);
                model.addRow(new Object[]{nama, ukm, namaKegiatan, waktuKegiatan});
            }

        } catch (Exception e) {
            e.printStackTrace();
        }
    }

